# Ввод массива A
print("Введите 10 элементов массива A:")
A = [float(input(f"A[{i}]: ")) for i in range(10)]

# Ввод массива B
print("Введите 10 элементов массива B:")
B = [float(input(f"B[{i}]: ")) for i in range(10)]

# Вывод исходных массивов
print("Исходный массив A:", A)
print("Исходный массив B:", B)

# Обмен содержимым массивов
A, B = B, A

# Вывод преобразованных массивов
print("Преобразованный массив A:", A)
print("Преобразованный массив B:", B)
