text = input("Введите текст: ")
word = input("Введите слово для поиска: ")

# Разбиваем текст на слова по пробелам (можно добавить обработку пунктуации при необходимости)
words = text.split()

# Считаем, сколько раз слово встречается (учитывается точное совпадение)
count = words.count(word)

print(f"Слово '{word}' встречается {count} раз(а).")
